package com.bridglabz.basicjavaSecA;

import com.bridglabz.utility.Utility;

public class TemperatureConversion {
	public static void main(String[]args) {
		float temperature;
	    System.out.println("Enter temperature in Fahrenheit:");
	    temperature = Utility.floatInput();
	    temperature = ((temperature - 32)*5/9);
	    System.out.println("Temperature in F to Celsius = " + temperature);
	    
	    float faranite;
	    System.out.println("Enter temprature in Celcious:");
	    faranite = Utility.floatInput();
	    faranite = ((faranite-32)*5)/9;
	    System.out.println("Temprature in C to Faranite="+ faranite);
	}
}
